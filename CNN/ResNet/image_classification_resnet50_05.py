le= preprocessing.LabelEncoder()

x['Actual']=le.fit_transform(x['Actual'])
x['Predicted']=le.fit_transform(x['Predicted'])

results = confusion_matrix(x['Actual'], x['Predicted'])
print(results)

plt.figure()
sns.heatmap(results, annot=True)
plt.xlabel('Actual')
